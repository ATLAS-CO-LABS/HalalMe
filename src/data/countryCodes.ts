// Worldwide dialling codes for phone-number entry.
//
// `code` is the ISO 3166-1 alpha-2 country code — this is what we store
// (as "GB:7911123456") and what Hyperzod expects as `country_code`.
// `dial` is the international dialling prefix, shown to the user only.
// The flag emoji is derived from `code` at render time (see flagEmoji).

export interface CountryCode {
  code: string; // ISO alpha-2, e.g. "GB"
  dial: string; // e.g. "+44"
  name: string;
}

/** Regional-indicator flag emoji from an ISO alpha-2 code, e.g. "GB" → 🇬🇧. */
export function flagEmoji(code: string): string {
  return code
    .toUpperCase()
    .replace(/[A-Z]/g, (c) => String.fromCodePoint(127397 + c.charCodeAt(0)));
}

